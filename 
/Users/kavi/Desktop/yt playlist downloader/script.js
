async function loadPlaylist() {
  const url = playlistUrlEl.value.trim();
  if (!PLAYLIST_ID_REGEX.test(url) && !VIDEO_ID_REGEX.test(url)) {
    showError('Invalid YouTube URL');
    return;
  }

  loadBtn.disabled = true;
  statusText.textContent = 'Loading playlist...';
  progress.classList.remove('hidden');
  videoList.innerHTML = '';

  for (const proxy of PROXIES) {
    try {
      statusText.textContent = `Trying proxy: ${proxy.split('/')[2]}...`;
      await fetchPlaylistData(url, proxy);
      if (playlistData.length > 0) {
        showVideoList();
        statusText.textContent = `Loaded ${playlistData.length} videos!`;
        return;
      }
    } catch (e) {
      console.log(`Proxy ${proxy} failed:`, e.message);
    }
  }

  // Fallback: Load mock data for demo
  loadMockData();
  showVideoList();
  statusText.textContent = 'Using demo data (real fetch failed - proxy issue)';
}

async function fetchPlaylistData(url, proxy) {
  let playlistId = url.match(PLAYLIST_ID_REGEX)?.[1];
  let targetUrl = playlistId ? `https://www.youtube.com/playlist?list=${playlistId}` : url;

  const controller = new AbortController();
  const timeoutId = setTimeout(() => controller.abort(), 10000);

  const response = await fetch(proxy + encodeURIComponent(targetUrl), { 
    signal: controller.signal,
    cache: 'no-cache'
  });
  clearTimeout(timeoutId);

  if (!response.ok) throw new Error(`HTTP ${response.status}`);

  const html = await response.text();
  
  // Multiple JSON extraction patterns
  const patterns = [
    /var ytInitialData = (\{.*?\});/s,
    /"ytInitialData":(\{.*?\}),/s,
    /ytInitialData\s*=\s*(\{.*?\});/s
  ];

  let dataMatch;
  for (const pattern of patterns) {
    dataMatch = html.match(pattern);
    if (dataMatch) break;
  }

  if (!dataMatch) throw new Error('ytInitialData not found');

  const data = JSON.parse(dataMatch[1]);
  playlistData = parsePlaylistVideos(data);
  
  if (playlistData.length === 0) throw new Error('No videos parsed');
}

