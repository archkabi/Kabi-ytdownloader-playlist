function parsePlaylistVideos(data) {
  const videos = [];
  
  const findVideos = (obj) => {
    if (!obj || typeof obj !== 'object') return;
    
    if (obj.playlistVideoRenderer) {
      const item = obj.playlistVideoRenderer;
      videos.push({
        title: getText(item.title?.runs),
        videoId: item.navigationEndpoint?.watchEndpoint?.videoId,
        thumb: item.thumbnail?.thumbnails?.[0]?.url || ''
      });
    } else if (obj.compactVideoRenderer) {
      const item = obj.compactVideoRenderer;
      videos.push({
        title: getText(item.title?.runs),
        videoId: item.videoId,
        thumb: item.thumbnail?.thumbnails?.[0]?.url || ''
      });
    }
    
    Object.values(obj).forEach(findVideos);
  };
  
  findVideos(data);
  return videos.slice(0, 25).filter(v => v.videoId);
}

function getText(runs) {
  return runs?.map(run => run.text).join('') || 'Unknown';
}

